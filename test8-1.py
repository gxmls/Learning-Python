'''
描述
获得用户的任何可能输入，将其中的英文字符进行打印输出，程序不出现错误。‪‬‪‬‪‬‪‬‪‬‮‬‫‬‭‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‭‬‪‬‪‬‪‬‪‬‪‬‮‬‭‬‪‬‪‬‪‬‪‬‪‬‪‬‮‬‫‬‭‬‪‬‪‬‪‬‪‬‪‬‮‬‪‬‮‬
输入输出示例
示例 1	
*&^123abc0e
abce
'''

s=input()
sn=''
for c in s:
    if 'a'<=c<='z' or 'A'<=c<="Z":
        sn+=c
    else:
        continue
print(sn)
